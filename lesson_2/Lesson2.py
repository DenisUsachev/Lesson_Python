# Генератор списков пример 1:
# Обычный способ:
list_6 = []
for i in range(1, 101):
    list_6.append(i)
print(list_6) # [1, 2, 3, 4, ... 100]
# С использованием генератора списков:
list_6 = [i for i in range(1, 101)] # [1, 2, 3, ... 100]

# Генератор списков пример 2:
list_6 = [i for i in range(1, 101) if i % 2 == 0] # [2, 4, 6, ... 100]

#Допустим, вы решили создать пары каждому из чисел (кортежи)
list_1 = [(i, i) for i in range(1, 101) if i % 2 == 0] # [(2, 2), (4, 4),...,(100, 100)]

#Также можно умножать, делить, прибавлять, вычитать. Например, умножитьзначение на 2.
