# Списки - упорядоченный конечный набор элементов:
list_1 = [] # Это пустой список
list_1 = list() # функиця лист, создает пустой список
print(list_1) # []
list_1 = [1, 2, 3, 8] # скиски со значениями
print(list_1) # [1, 2, 3, 8]
print(*list_1) # Чтобы вывод был без квадратных скобочок и запятых

for i in list_1:
    print(i) # 1 2 3 8

print(len(list_1)) # 4 элемента

print(list_1[0]) # index 3 = 8
print(list_1[-1]) # index 3 = 8

